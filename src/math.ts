/**
 * 加法
 *
 * @param a 第一个数
 * @param b 第二个数
 * @returns 两个数的和
 */
export const add = (a: number, b: number) => a + b

/**
 * 减法
 *
 * @param a 第一个数
 * @param b 第二个数
 * @returns 两个数的差
 */
export const subtract = (a: number, b: number) => a - b

/**
 * 乘法
 *
 * @param a 第一个数
 * @param b 第二个数
 * @returns 两个数的乘积
 */
export const mul = (a: number, b: number) => a * b
